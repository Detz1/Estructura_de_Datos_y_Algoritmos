    private void btn_runActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_runActionPerformed
        long inicio=0;
        long fin=0;
        long resultado= 0;
        
        
        if(btn_recursivo.isSelected()){
            int z= array.length;    
            inicio=System.nanoTime();
            resultado=metodo.sum_Recursivo(array, (z-1));
            fin=System.nanoTime();
            this.text_elementos.setText(metodo.printVector(array)+" = "+resultado);
            model.addRow(new Object[]{array.length,"Recursivo",inicio,fin,fin-inicio});
            
        }else if(btn_iterativo.isSelected()){    
            inicio=System.nanoTime();
            resultado=metodo.sum_Iterativo(array);
            fin=System.nanoTime();
    
            this.text_elementos.setText(metodo.printVector(array)+" = "+resultado);
             model.addRow(new Object[]{array.length,"Iterativo",inicio,fin,fin-inicio});
        }else{
                JOptionPane.showMessageDialog(null, "SIN SELECCION");
                }
        n_tabla.setModel(model);
    }//GEN-LAST:event_btn_runActionPerformed

