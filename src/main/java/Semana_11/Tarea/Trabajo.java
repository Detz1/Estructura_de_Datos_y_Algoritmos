class Metodo{
        
    static int nAleatorio(int min, int max){ // DEVUELVE UN NUMERO ALEATORIO
        int n;
        Random rd=new Random();
        n=rd.nextInt((max-min)+1)+min;
        return n;      
    }
    void rellenarVector(int vector[]){
        for (int i = 0; i <vector.length ; i++) {
            vector[i]=nAleatorio(1,20);
        }
    }
    
    String printVector(int vector[]){ 
        String x="";
        for(Integer i:vector){
         x+=" [ "+i+" ]";  
        }
        return x;
    }
    
    int sum_Recursivo(int vector[],int n) {
            int rta;
            if (n == 0){
                return vector[n];
            }else{
                 rta =  vector[n] +  sum_Recursivo( vector,(n-1));
            }
            return rta;
        }
    
    int sum_Iterativo(int vector[]){
        int sum=0;
        for (int i = 0; i < vector.length; i++) {
            sum +=vector[i];
        }
        
        return sum;
    }
}

