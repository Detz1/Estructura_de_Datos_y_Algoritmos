class Complementos{
        
    static int nAleatorio(int min, int max){ // DEVUELVE UN NUMERO ALEATORIO
        int n;
        Random rd=new Random();
        n=rd.nextInt((max-min)+1)+min;
        return n;      
    }
    
    static String cadenaAleatoria(int longitud) { // DEVUELVE UN NOMBRE ALEATORIO
        // El banco de caracteres
        String banco = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ";
        // La cadena en donde iremos agregando un car√°cter aleatorio
        String cadena = "";
        for (int x = 0; x < longitud; x++) {
            int indiceAleatorio = nAleatorio(0, banco.length() - 1);
            char caracterAleatorio = banco.charAt(indiceAleatorio);
            cadena += caracterAleatorio;
        }
        return cadena;
    }  
}


